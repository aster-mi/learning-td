def ts_entry(q):
    def esc(s):
        return s.replace('\\','\\\\').replace('"','\\"').replace('\n','\\n')
    cs = ', '.join(f'"{esc(c)}"' for c in q['choices'])
    return (f'  {{ id: "{q["id"]}", main: "{esc(q["main"])}", sub: "{esc(q["sub"])}", '
            f'level: {q["level"]}, question: "{esc(q["question"])}", '
            f'choices: [{cs}], answer: "{esc(q["answer"])}" }},')

